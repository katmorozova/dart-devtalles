/**
 * un operador es un simbolo que le dice al compilador
 * que debe de realizar una tarea
 * matematica, relacional o logica
 * y debe de producir un resultado
 */


main() {
  int a = 10 + 5; // + 15
  a = 20 - 10; // - 10
  a = 10 * 2; // * 20


  double b = 10 / 2; //  / 5
  b = 10.0 % 3; // 1
  b = -b; // -b  es usado para cambiar el valor de la b


  int c = 10 ~/ 3; // ~/ 3 division comun y parte entera


  int d = 1;


  d++;
  d--;
  d += 2;
 
}
